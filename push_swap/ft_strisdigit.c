#include "push_swap.h"
#include <stdio.h>
int	ft_strisdigit(char *s)
{
	int	i;

	i = -1;
	while(s[++i])
	{
		if (s[i] < 48 || s[i] > 57)
			return (0);
	}
	return(1);
}