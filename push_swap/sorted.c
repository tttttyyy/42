#include "push_swap.h"

int sorted(char **sp)
{
    int i;

    i = 0;
    while(sp && sp[i + 1])
    {
        if(ft_atoi(sp[i]) > ft_atoi(sp[i + 1]))
            return(0);
        i++;
    }
    return(1);
}