#include "push_swap.h"

long int	ft_atoi(const char *str)
{
	long int		res;
	size_t			i;
	size_t			sign;

	i = 0;
	sign = 0;
	res = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			sign = 1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + str[i] - 48;
		i++;
	}
	if (sign == 1)
		return (-res);
	return (res);
}
